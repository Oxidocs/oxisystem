          <p>Hace unos meses, las académicas de la carrera Mg. Carolina Rodríguez Malebrán y Mg. Marcela Altamirano, se encontraron con la gratificante noticia que su investigación de casi dos años fue publicada por la revista Actualidades Investigativas en Educación de la Universidad de Costa Rica y que fue indexada al prestigioso sitio Scientific Electronic Library Online (SciElo). El trabajo que lleva por nombre “TELEVISIÓN EDUCATIVA Y DESASTRES NATURALES: ESCOLARES CHILENOS Y SUS PROFESORAS VALORAN USO DEL VIDEO EDUCATIVO EN EL AULA”  fue un proyecto realizado en conjunto con la académica del Departamento de Matemáticas,  Margarita García y del Ex Centro de Informática la Mg. Karina Nuñez. Esta investigación también tuvo como participantes a los alumnos de la generación 2013 que participaron en el Electivo de Producción Educativa con la creación del programa ‘Benvoy y las Hormigas’.</p>

          <p>Este electivo trató sobre cómo se generan los desastres y catástrofes naturales, que fueron temas de conversación a comienzo de esta década y tuvo como fin explicar métodos de prevención antes estos eventos a estudiantes de enseñanza básica. Luego de este primer proceso las académicas pusieron en marcha su investigación validando los instrumentos de investigación (los capítulos de Benvoy y las Hormigas) en escuela de Caleta San Pedro y posteriormente aplicaron las encuestas en la Escuela David Tapia León del pueblo de Tongoy. La realización de esta investigación también fue posible debido a que el proyecto fue ganador de los fondos entregados por la Dirección de Investigación de la Universidad de La Serena, DIULS.</p>

          <p>“Nosotras estuvimos un par de años, es bastante demoroso el proceso hasta que uno publica, a lo mejor en otras áreas como Ciencias son más habituales las publicaciones, acá costó esta primera publicación, yo espero que después de esta hayan más” indicó Carolina Rodríguez  respecto a este hecho, ya que este tipo de investigaciones también aportan a los procesos de acreditación tanto institucionales como a nivel de carrera.</p>

          <h3>Un proceso extenso pero gratificante</h3>

          <p>Aproximadamente dos años fue el proceso entre la puesta en marcha del proyecto y su publicación final en Mayo del 2016. Tras varios procesos de minuciosa revisión por parte de los pares ciegos que evalúan los artículos a publicar en SciElo “tuvimos que hacer las correcciones, enviamos de nuevo el artículo, lo devolvieron otra vez, lo enviamos otra vez y por último pasó por la revisión de una filóloga, quien es la que le da la última revisión en cuanto al estilo lingüístico del texto” detalló Rodríguez al respecto.</p>

          <p>“La investigación que llevó a esta publicación indexada es resultado de un arduo trabajo durante más de un año y es sabido que este tipo de revistas realiza una evaluación muy minuciosa de los artículos antes de publicar, desde esa perspectiva es un logro académico importante” señaló Marcela Altamirano y además agregó que “Destaco dos aspectos que considero relevantes. Lo primero, es el buen trabajo en equipo, esto nos ha permitido continuar con la línea de investigación y pensar en desarrollar otras que resulten significativas para la Escuela de Periodismo. Lo segundo, es resaltar que la investigación es compatible con la docencia, si se aprovecha lo aprendido para fortalecer la formación de nuestros estudiantes y eso, para mí, es lo fundamental” finalizó.</p>

          <p>Cabe recordar que el sitio SciElo es una red de Librerías Online que alberga un gran número de artículos científicos a lo largo del mundo y de gran prestigio. El trabajo desarrollado por las académicas de la carrera es uno de los primeros artículos relacionados a la ciencia social que ha sido emitido desde la Universidad.</p>
